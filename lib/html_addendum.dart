class _EventStreamProvider<T> {
  EventTarget _target;
  String _type;
  StreamController<T> _controller;
  EventListener _handler;

  int _count = 0;
  void _startListen() {
    if (_count == 0) {
      _handler = (x) {
        _controller.add(x as T);
      };
      _target.addEventListener(_type, _handler);
    }
    _count++;
  }

  void _stopListen() {
    _count--;
    if (_count == 0 && _handler != null) {
      _target.removeEventListener(_type, _handler);
      _handler = null;
    }
  }

  _EventStreamProvider(EventTarget tgt, String type)
      : _target = tgt,
        _type = type {
    _controller = new StreamController.broadcast(onListen: _startListen, onCancel: _stopListen);
  }
}

class EventStreamProvider<T> {
  final String _type;

  const EventStreamProvider(String type) : _type = type;

  Stream<T> forTarget(EventTarget tgt) => new _EventStreamProvider(tgt, _type)._controller.stream;

  call(EventTarget t) => forTarget(t);
}

