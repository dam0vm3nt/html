        return _KeyName.RIGHT;
      case KeyCode.SCROLL_LOCK:
        return _KeyName.SCROLL;
      case KeyCode.SHIFT:
        return _KeyName.SHIFT;
      case KeyCode.SPACE:
        return _KeyName.SPACEBAR;
      case KeyCode.TAB:
        return _KeyName.TAB;
      case KeyCode.UP:
        return _KeyName.UP;
      case KeyCode.WIN_IME:
      case KeyCode.WIN_KEY:
      case KeyCode.WIN_KEY_LEFT:
      case KeyCode.WIN_KEY_RIGHT:
        return _KeyName.WIN;
      default:
        return _KeyName.UNIDENTIFIED;
    }
    return _KeyName.UNIDENTIFIED;
  }
}

bool matchesWithAncestors(HTMLElement elem, String selectors) {
  do {
    if (elem.matches(selectors)) return true;
    elem = elem.parentElement;
  } while (elem != null);
  return false;
}

List<Node> asList(NodeList nodeList) => new List.from(asIterable(nodeList));

Iterable<Node> asIterable(NodeList nodeList) sync* {
  for (int i = 0; i < nodeList.length; i++) yield nodeList[i];
}
